    private void btnEditarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarTareaActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaTareas.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona una tarea para editar.");
            return;
        }

        // Obtener el ID desde la primera columna
        int idTarea = Integer.parseInt(tablaTareas.getValueAt(filaSeleccionada, 0).toString());

        // Buscar la tarea desde la base de datos
        TareaDAO dao = new TareaDAO();
        Tarea tareaEncontrada = dao.buscarPorId(idTarea);

        // Si la encuentra, abre el formulario con los datos
        if (tareaEncontrada != null) {
            AgregarTarea editarForm = new AgregarTarea(tareaEncontrada);
            editarForm.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró la tarea.");
        }
    }//GEN-LAST:event_btnEditarTareaActionPerformed

    private void btnEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTareaActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaTareas.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una tarea para eliminar.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
                this,
                "¿Estás seguro de que deseas eliminar esta tarea?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Obtener ID oculto de la tarea desde la columna 0
            int idTarea = (int) tablaTareas.getValueAt(filaSeleccionada, 0);

            // Eliminar de la base de datos
            TareaDAO dao = new TareaDAO();
            Tarea tarea = dao.obtenerTareaPorId(idTarea);
            dao.eliminarTarea(tarea);

            // Recargar visualmente la tabla
            cargarListaDeTareas();

            JOptionPane.showMessageDialog(this, "Tarea eliminada correctamente.");
            actualizarContadorTareas();
        }
    }//GEN-LAST:event_btnEliminarTareaActionPerformed

