    private void btnGuardarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTareaActionPerformed
        // TODO add your handling code here:

        try {
            String titulo = txtAgregarTituloTarea.getText().trim();
            String descripcion = txtAgregarDescripcionTarea.getText().trim();
            Date fechaVencimiento = dateAgregarFechaEntregaTarea.getDate();
            String nombreCategoria = (String) boxAgregarCategoriaTarea.getSelectedItem();
            String nombrePrioridad = (String) boxAgregarPrioridadTarea.getSelectedItem();
            String nombreUsuario = (String) boxAgregarUsuarioTarea.getSelectedItem();
            
            if (titulo.isEmpty() || descripcion.isEmpty() || fechaVencimiento == null
                    || nombreCategoria == null || nombrePrioridad == null || nombreUsuario == null
                    || nombreCategoria.equals("Seleccione una categoría")
                    || nombrePrioridad.equals("Seleccionar una prioridad")
                    || nombreUsuario.equals("Seleccione un usuario")) {
                JOptionPane.showMessageDialog(this, "Por favor completa todos los campos.");
                return;
            }

            // Buscar los objetos completos a partir del nombre
            
            Categoria categoria = new CategoriaDAO().buscarPorNombre(nombreCategoria);
            Prioridad prioridad = new PrioridadDAO().buscarPorNombre(nombrePrioridad);
            Usuario usuario = new UsuarioDAO().buscarPorNombre(nombreUsuario);


            if (categoria == null || prioridad == null || usuario == null) {
                JOptionPane.showMessageDialog(this, "No se pudo obtener los datos seleccionados.");
                return;
            }

            // Si estás editando una tarea existente
            if (editarTarea != null) {
                editarTarea.setTitulo(titulo);
                editarTarea.setDescripcion(descripcion);
                editarTarea.setFechaVencimiento(fechaVencimiento);
                editarTarea.setIdCategoria(categoria);
                editarTarea.setIdPrioridad(prioridad);
                editarTarea.setIdUsuario(usuario);
                new TareaDAO().actualizarTarea(editarTarea);
                JOptionPane.showMessageDialog(this, "Tarea actualizada exitosamente.");
            } else {
                // Nueva tarea
                Tarea nuevaTarea = new Tarea();
                nuevaTarea.setTitulo(titulo);
                nuevaTarea.setDescripcion(descripcion);
                nuevaTarea.setFechaVencimiento(fechaVencimiento);
                nuevaTarea.setEstado("pendiente");
                nuevaTarea.setIdCategoria(categoria);
                nuevaTarea.setIdPrioridad(prioridad);
                nuevaTarea.setIdUsuario(usuario);
                new TareaDAO().guardarTarea(nuevaTarea);
                JOptionPane.showMessageDialog(this, "Tarea guardada exitosamente.");
            }

            new VistaAdministrador().setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar/actualizar tarea: " + e);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarTareaActionPerformed

