    public AgregarTarea(Tarea tarea) {
        initComponents();
        this.editarTarea = tarea;

        cargarCategorias();
        cargarPrioridades();
        cargarUsuarios();

        // precargar los campos con los datos de la tarea
        if (tarea != null) {
            txtAgregarTituloTarea.setText(tarea.getTitulo());
            txtAgregarDescripcionTarea.setText(tarea.getDescripcion());
            txtAgregarComentarioTarea.setText(""); 
            dateAgregarFechaEntregaTarea.setDate(tarea.getFechaVencimiento());
            boxAgregarCategoriaTarea.setSelectedItem(tarea.getIdCategoria().getNombreCategoria());
            boxAgregarPrioridadTarea.setSelectedItem(tarea.getIdPrioridad().getNombrePrioridad());
            boxAgregarUsuarioTarea.setSelectedItem(tarea.getIdUsuario().getNombre());
        }
    }

    private void cargarCategorias() {
        CategoriaDAO categoriaDAO = new CategoriaDAO();
        try {
            List<Categoria> categorias = categoriaDAO.buscarTodasLasCategorias();
            boxAgregarCategoriaTarea.removeAllItems();
            boxAgregarCategoriaTarea.addItem("Seleccionar una categoría");
            for (Categoria c : categorias) {
                boxAgregarCategoriaTarea.addItem(c.getNombreCategoria()); 
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar categorías: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AgregarTarea.class.getName()).log(Level.SEVERE, "Error al cargar categorías", e);
        }
    }

    private void cargarPrioridades() {
        PrioridadDAO prioridadDAO = new PrioridadDAO();
        try {
            List<Prioridad> prioridades = prioridadDAO.buscarTodasLasPrioridades();
            boxAgregarPrioridadTarea.removeAllItems();
            boxAgregarPrioridadTarea.addItem("Seleccionar una prioridad");
            for (Prioridad p : prioridades) {
                boxAgregarPrioridadTarea.addItem(p.getNombrePrioridad());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar prioridades: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AgregarTarea.class.getName()).log(Level.SEVERE, "Error al cargar prioridades", e);
        }
    }

    private void cargarUsuarios() {
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        try {
            List<Usuario> usuarios = usuarioDAO.buscarTodosLosUsuarios();
            boxAgregarUsuarioTarea.addItem("Seleccione un usuario");
            for (Usuario u : usuarios) {
                boxAgregarUsuarioTarea.addItem(u.getNombre());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar usuarios: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(AgregarTarea.class.getName()).log(Level.SEVERE, "Error al cargar usuarios", e);
        }
    }

