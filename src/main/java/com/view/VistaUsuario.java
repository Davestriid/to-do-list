    private void btnEditarTareaUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarTareaUsuarioActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaTareasUsuario.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor selecciona una tarea para editar.");
            return;
        }

        // Obtener el ID desde la primera columna
        int idTarea = Integer.parseInt(tablaTareasUsuario.getValueAt(filaSeleccionada, 0).toString());

        // Buscar la tarea desde la base de datos
        TareaDAO dao = new TareaDAO();
        Tarea tareaEncontrada = dao.buscarPorId(idTarea);

        // Si la encuentra, abre el formulario con los datos
        if (tareaEncontrada != null) {
            AgregarTarea editarForm = new AgregarTarea(tareaEncontrada);
            editarForm.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró la tarea.");
        }
    }//GEN-LAST:event_btnEditarTareaUsuarioActionPerformed

