    private void cargarListaDeTareas() {
        TareaDAO dao = new TareaDAO();
        this.listaDeTareas = dao.obtenerTodasLasTareas(); 

        String[] columnas = {"ID", "Título", "Usuario","Descripción", "Fecha Vencimiento", "Estado", "Comentarios"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas);

        for (Tarea t : listaDeTareas) {
            Object[] fila = {
                t.getIdTarea(), 
                t.getTitulo(),
                t.getIdUsuario(),
                t.getDescripcion(),
                t.getFechaVencimiento(),
                t.getEstado(),
                t.getComentariosList()
            };
            modelo.addRow(fila);
        }
        
        
        // para actualizar los datos de la tabla
        tablaTareasUsuario.setModel(modelo);
        
        //para ocultar visualmente el id
        tablaTareasUsuario.getColumnModel().getColumn(0).setMinWidth(0);
        tablaTareasUsuario.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaTareasUsuario.getColumnModel().getColumn(0).setWidth(0);
    }
    
    private void btnEliminarTareaUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTareaUsuarioActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = tablaTareasUsuario.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una tarea para eliminar.");
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(
            this,
            "¿Estás seguro de que deseas eliminar esta tarea?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION
        );

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Obtener ID oculto de la tarea desde la columna 0
            int idTarea = (int) tablaTareasUsuario.getValueAt(filaSeleccionada, 0);

            // Eliminar de la base de datos
            TareaDAO dao = new TareaDAO();
            Tarea tarea = dao.obtenerTareaPorId(idTarea);
            dao.eliminarTarea(tarea);

            // Recargar visualmente la tabla
            cargarListaDeTareas();

            JOptionPane.showMessageDialog(this, "Tarea eliminada correctamente.");
        }
    }//GEN-LAST:event_btnEliminarTareaUsuarioActionPerformed

